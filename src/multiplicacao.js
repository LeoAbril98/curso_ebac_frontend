"use strict";
// Função de multiplicação
function multiplicacao(num1, num2) {
    return num1 * num2;
}
// Função de saudação
function saudacao(nome) {
    return "Olá " + nome;
}
// Exemplos de uso das funções
console.log(multiplicacao(3, 15)); // Saída: 12
console.log(saudacao("Leonardo")); // Saída: Olá Joãonode
